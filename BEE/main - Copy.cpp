void Shiba_Process()
{
    while (test--)
    {
        double a, b, bee, s; cin >> a >> b >> bee >> s;

        if (bee <= a)
        {
            double ti = s / (a + b);
            cout << int(bee * ti) << '\n';
        }

        else if (bee <= b)
        {
            double ti = s / (bee + b);
            double Sbee = bee * ti, Sb = b * ti, Sa = a * ti;
            ti = (s - Sb - Sa) / (a + b);
            cout << int(Sbee + bee * ti) << '\n';
        }

        else
        {
            bool Right = false;
            double Ta = 0, Tbee = 0, Tb = s, res = 0;
            while (true)
            {
                if (!Right)
                {
                    double ti = (Tb - Tbee) / (b + bee);
                    Tb -= b * ti;
                    Ta += a * ti;
                    Tbee += bee * ti;
                    res += bee * ti;
                    Right = true;
                }
                else
                {
                    double ti = (Tbee - Ta) / (a + bee);
                    Tb -= b * ti;
                    Ta += a * ti;
                    Tbee -= bee * ti;
                    res += bee * ti;
                    Right = false;
                }
                if (Tb - Ta < 0.0001) break;
            }
            cout << int(res) << '\n';
        }
    }
}

