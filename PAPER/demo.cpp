#define MAX 1100

int n, k, m, row[] = {-1, 0, 1, 0}, col[] = {0, 1, 0, -1};
long long F[2][2][2][2], T[2][2][2][2];
bool dx[MAX][MAX], a[MAX][MAX], l, r, u, d;
int p[33];

void Shiba_Read()
{
    cin >> n >> k >> m;
    while (m--)
    {
        int x, y; cin >> x >> y;
        a[x][y] = true;
    }

    p[0] = 1;
    for (int i = 1; i <= n; ++i) p[i] = p[i - 1] * 2;
}

bool Shiba_Check(int x, int y)
{
    return (x >= 1 && x <= p[k] && y >= 1 && y <= p[k] && !dx[x][y] && !a[x][y]);
}

void Shiba_Dfs(int x, int y)
{
    dx[x][y] = true;
    if (x == 1) u = true;
    if (x == p[k]) d = true;
    if (y == 1) l = true;
    if (y == p[k]) r = true;

    for (int i = 0; i < 4; ++i)
    {
        int p = x + row[i], q = y + col[i];
        if (Shiba_Check(p, q)) Shiba_Dfs(p, q);
    }
}

void Shiba_Process()
{
    for (int i = 0; i <= 1; ++i)
    for (int j = 0; j <= 1; ++j)
    for (int x = 0; x <= 1; ++x)
    for (int y = 0; y <= 1; ++y)
        F[i][j][x][y] = 0;

    for (int i = 1; i <= p[k]; ++i) for (int j = 1; j <= p[k]; ++j) if (Shiba_Check(i, j))
    {
        l = r = u = d = false;
        Shiba_Dfs(i, j);
        F[l][r][u][d] += 1;
    }

    for (int i = 1; i <= n - k; ++i)
    {
        for (int i = 0; i <= 1; ++i)
        for (int j = 0; j <= 1; ++j)
        for (int x = 0; x <= 1; ++x)
        for (int y = 0; y <= 1; ++y)
            T[i][j][x][y] = 0;

        for (int x = 0; x <= 1; ++x) for (int y = 0; y <= 1; ++y)
        {
            T[0][0][x][y] += F[1][0][x][y];

            T[1][0][x][y] += F[0][1][x][y];
            T[0][0][x][y] += F[0][0][x][y];
        }

        for (int x = 0; x <= 1; ++x) for (int y = 0; y <= 1; ++y)
            F[1][0][x][y] = 0;

        for (int i = 0; i <= 1; ++i)
        for (int j = 0; j <= 1; ++j)
        for (int x = 0; x <= 1; ++x)
        for (int y = 0; y <= 1; ++y)
            F[i][j][x][y] += T[i][j][x][y];

        ///******************************************************///

        for (int i = 0; i <= 1; ++i)
        for (int j = 0; j <= 1; ++j)
        for (int x = 0; x <= 1; ++x)
        for (int y = 0; y <= 1; ++y)
            T[i][j][x][y] = 0;

        for (int x = 0; x <= 1; ++x) for (int y = 0; y <= 1; ++y)
        {
            T[x][y][0][0] += F[x][y][0][1];

            T[x][y][0][1] += F[x][y][1][0];
            T[x][y][0][0] += F[x][y][0][0];
        }

        for (int x = 0; x <= 1; ++x) for (int y = 0; y <= 1; ++y)
            F[x][y][0][1] = 0;

        for (int i = 0; i <= 1; ++i)
        for (int j = 0; j <= 1; ++j)
        for (int x = 0; x <= 1; ++x)
        for (int y = 0; y <= 1; ++y)
            F[i][j][x][y] += T[i][j][x][y];
    }

    long long ans = 0;
    for (int i = 0; i <= 1; ++i)
    for (int j = 0; j <= 1; ++j)
    for (int x = 0; x <= 1; ++x)
    for (int y = 0; y <= 1; ++y)
        ans += F[i][j][x][y];

    cout << ans;
}

